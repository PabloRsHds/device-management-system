package br.com.analysis.enums;

public enum Unit {

    // Temperature
    CELSIUS,          // °C
    FAHRENHEIT,       // °F
    KELVIN,           // K

    // Humidity
    PERCENTAGE,       // %

    // Gas & Air Quality
    PPM,              // Parts per million
    PPB,              // Parts per billion
    MG_M3,            // mg/m³

    // Pressure
    PASCAL,           // Pa
    HECTOPASCAL,      // hPa
    BAR,              // bar
    PSI,              // psi

    // Distance / Position
    METER,            // m
    CENTIMETER,       // cm
    MILLIMETER,       // mm

    // Speed / Flow
    LITER_PER_MINUTE, // L/min
    METER_PER_SECOND, // m/s

    // Light
    LUX,              // lux

    // Sound
    DECIBEL,          // dB

    // Force
    NEWTON,           // N
    KILOGRAM_FORCE,   // kgf

    // Misc
    VOLT,             // V
    AMPERE,           // A
    OHM;              // Ω
}
